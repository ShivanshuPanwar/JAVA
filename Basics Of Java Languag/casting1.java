public class casting1 {
    public static void main(String args[])
    {
        char ch = 'a';
        char ch2 = 'b';
        char ch3 = 'z';
        char ch4 = 'A';
        char ch5 = 'Z';
        int number = ch;
        int number2 = ch2;
        int number3 = ch3;
        int number4 = ch4;
        int number5 = ch5;
        System.out.println(number);
        System.out.println(number2);
        System.out.println(number3);
        System.out.println(number4);
        System.out.println(number5);
    }
    
}
